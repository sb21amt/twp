                        <p>
 
Lasting Powers of Attorney were created by the government in the Mental Capacity Act 2005.
 <br>
They came into force on 1st October 2007, replacing the similar previous document – the Enduring Power of Attorney. The word “lasting” in this context means that the power may continue even if the person (though still alive) no longer has the mental capacity to exercise the power to choose an attorney.
 <br>
The format of the prescribed forms was changed in 2009. The old forms were much longer, with PFA LPAs being 28 pages long and HW LPAs being 24 pages long.
 <br>
On 1st October 2013, the fees for registering LPAs was cut from £130 each to £110 each.
 <br>
On 1st July 2015, the Office of the Public Guardian released new LPA forms. These became the compulsory format for any LPAs created on or after 1st January 2016. Industry feedback to the new forms has been positive, with most people agreeing that they are simpler and clearer. They also removed two factors that had previously been necessary in order to complete the forms.</p>
