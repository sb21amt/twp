<h2>Health and Welfare LPA</h2> 
<p> 
The powers conveyed by the Health and Welfare LPA are much less understood, and often less appreciated than the Property & Financial LPA. Yet, they can often become the more urgent and life-changing powers that are necessary to sustain your quality of life.
 Making decisions about the donor’s permanent residence
 Deciding if care is required and what level of care should be provided
 Consent or refusal of medical treatment
 Rights of access to personal information
